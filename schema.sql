-- ── updated_at trigger ────────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION trigger_set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS set_updated_at ON users;
CREATE TRIGGER set_updated_at
  BEFORE UPDATE ON users
  FOR EACH ROW EXECUTE FUNCTION trigger_set_updated_at();

-- ── Cleanup function (replaces in-memory setInterval) ────────────────────────
-- Call periodically from the application or a pg_cron job.

CREATE OR REPLACE FUNCTION cleanup_expired_tokens()
RETURNS INTEGER AS $$
DECLARE
  deleted INTEGER;
BEGIN
  DELETE FROM refresh_tokens WHERE expires_at < now();
  GET DIAGNOSTICS deleted = ROW_COUNT;
  RETURN deleted;
END;
$$ LANGUAGE plpgsql;

GRANT EXECUTE ON FUNCTION cleanup_expired_tokens() TO patchticker_app;

CREATE OR REPLACE FUNCTION cleanup_stale_lockouts(window_seconds INTEGER DEFAULT 1800)
RETURNS INTEGER AS $$
DECLARE
  deleted INTEGER;
BEGIN
  DELETE FROM account_lockouts
  WHERE
    (locked_until IS NULL AND first_attempt_at < now() - (window_seconds || ' seconds')::INTERVAL)
    OR (locked_until IS NOT NULL AND locked_until < now());
  GET DIAGNOSTICS deleted = ROW_COUNT;
  RETURN deleted;
END;
$$ LANGUAGE plpgsql;

GRANT EXECUTE ON FUNCTION cleanup_stale_lockouts(INTEGER) TO patchticker_app;
